# But in a script, an expression all by itself doesn't do anything! This is a common source od confusion for beginners.
# Type the following statements in the Python interpreter to see what they do:

5 # At interactive mode when we type "5" nothing important happens, only show the number 5
x = 5 # When we type "x = 5" the user don't see anything but a line break
x = 1 # Now, when type the expression "x + 1" and press enter key, the prompt show us the number 6 in the next line. Thus, we understand that the value store in value x (5) add one, and the result is 6
