# Assume that we execute the following assignment statements:

width = 17
height = 12.0

one = width // 2
print(one, type(one)) # 8 and type is <class 'int'>

two = width / 2.0
print(two, type(two)) # 8.5 and type is <class 'float'>

three = height / 3
print(three, type(three)) # 4.0 and type is <class 'float'>

four = 1 + 2 * 5
print(four, type(four)) # 11 and type is <class 'int'>
